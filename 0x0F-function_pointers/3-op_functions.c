/**
 * op_add - add function
 * @a: integer input
 * @b: integer input
 *
 * Return: Sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract function
 * @a: integer input
 * @b: integer input
 *
 * Return: Subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply function
 * @a: integer input
 * @b: integer input
 *
 * Return: multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: integer input
 * @b: integer input
 *
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo operation
 * @a: integer input
 * @b: integer input
 *
 * Return: Modulus a,b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
